package Loops;

public class PrintNumbers1To10 {

	public static void main(String[] args) {
		
		
		//print numbers from 1 to 10
				//number initialized to 1
		        int num = 1;
		        
		        //condition to check number is less than equal to 10
		        while (num <= 10)
		        {
		            System.out.println("Number is:"+num);
		            num++;
		        }
		    }
}

